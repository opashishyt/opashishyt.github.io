// Utility function to get app by ID
function getAppById(id) {
    return appsData.find(app => app.id == id);
}

// Utility function to search apps
function searchApps(query) {
    const searchLower = query.toLowerCase();
    return appsData.filter(app => {
        return app.name.toLowerCase().includes(searchLower) ||
               app.description.toLowerCase().includes(searchLower) ||
               (app.features && app.features.some(f => f.toLowerCase().includes(searchLower)));
    });
}

